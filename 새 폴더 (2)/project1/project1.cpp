#include <iostream>        // 입출력 사용을 위한 헤더 포함
using namespace std;       // std:: 생략하고 사용하기 위해 선언

int main() {
	int A[2][2] = { {2,4}, {5, -5} }; //배열 초기화
	int B[2][2] = { {-2,3}, {0, -5} }; //배열 초기화

	// 결과를 저장할 2x2 크기의 정수형 배열 C 선언
	int C[2][2];

	// A와 B의 각 요소를 더해 C에 저장하는 이중 for문
	for (int i = 0; i < 2; i++) {        // i는 행(index 0~1)
		for (int j = 0; j < 2; j++) {    // j는 열(index 0~1)
			C[i][j] = A[i][j] + B[i][j]; // 같은 위치의 원소끼리 더함
		}
	}

	cout << "연산결과" << endl;  // 연산 결과 출력 안내문

	// 결과 배열 C의 모든 값 출력
	for (int i = 0; i < 2; i++) {        // 행 반복
		for (int j = 0; j < 2; j++) {    // 열 반복
			cout << C[i][j] << ' ';      // 해당 위치의 값 출력 후 공백
		}
		cout << endl;                    // 한 행 출력 후 줄바꿈
	}
}


// **********************************************
//제 목 : 원소의 최대값과 위치출력
//날 짜 : 2025 년 12 월 1 일
//작성자 : 2201052 최재민
// **********************************************

#include <iostream>  // 입출력 사용을 위한 헤더
using namespace std; // std:: 생략하고 사용하기 위해 선언 

int main() {
	// 3x3 배열 초기화
	int arr[3][3] = { { -5,  2,  35 },{ -20, 5, 100 },{ -75, 5, -25 } };

	int maxVal = arr[0][0]; // 최대값을 배열 첫 번째 값으로 초기화

	// 최대값 위치 초기화 (행, 열). 사용자에게 보여줄 때 1부터 시작하도록 설정
	int maxRow = 1, maxCol = 1;

	// 2중 for문으로 배열 전체 탐색
	for (int i = 0; i < 3; i++) {
		for (int j = 0; j < 3; j++) {
			if (arr[i][j] > maxVal) {      // 현재 값이 최대값보다 크면
				maxVal = arr[i][j];       // 최대값 갱신
				maxRow = i + 1;           // 행 번호 저장 
				maxCol = j + 1;           // 열 번호 저장 
			}
		}
	}

	// 결과 출력
	cout << "최대값은 " << maxVal << endl;
	cout << "위치는 " << maxRow << "행 " << maxCol << "열" << endl;
}