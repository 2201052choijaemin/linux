#include <iostream>  // 입출력 사용을 위한 헤더
using namespace std; // std:: 생략하고 사용하기 위해 선언 


int main() {
	// 원본 3x3 배열 초기화
	int arr[3][3] = { { -5,  2,  35 },{ -20, 5, 100 },{ -75, 5, -25 } };

	int mod[3][3];  // 변환된 값을 저장할 배열

	// 2중 for문으로 배열 전체 탐색
	for (int i = 0; i < 3; i++) {
		for (int j = 0; j < 3; j++) {
			if (arr[i][j] < 0)          // 원소가 음수이면
				mod[i][j] = 0;          // 0으로 변환
			else                        // 원소가 0 이상이면
				mod[i][j] = 255;        // 255로 변환
		}
	}

	cout << "수정 행렬" << endl;

	// 변환된 배열 출력
	for (int i = 0; i < 3; i++) {       // 행 반복
		for (int j = 0; j < 3; j++) {   // 열 반복
			cout << mod[i][j] << " ";   // 각 원소 출력 후 공백
		}
		cout << endl;                   // 한 행 출력 후 줄바꿈
	}
}