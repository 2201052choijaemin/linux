#include <iostream>  // 입출력 사용을 위한 헤더
using namespace std; // std:: 생략하고 사용하기 위해 선언 

int main() {
	int score[3][3];      // 3명의 학생 × 3과목(국어, 영어, 수학) 성적 저장 배열
	double avg[3];        // 각 학생의 평균 점수를 저장할 배열
	int sum;              // 각 학생의 점수 합계 저장용 변수
	int bestIndex = 0;    // 최우수 학생의 인덱스 (초기값 0: 첫 번째 학생)

	// 학생 성적 입력
	for (int i = 0; i < 3; i++) {                         // 3명의 학생 반복
		cout << i + 1 << "번째 학생의 국어 영어 수학 성적을 입력: ";
		for (int j = 0; j < 3; j++) {                     // 3과목 반복
			cin >> score[i][j];                            // 입력값 배열에 저장
		}
	}

	// 각 학생의 평균 계산
	for (int i = 0; i < 3; i++) {                         // 학생 반복
		sum = 0;                                         // 학생 점수 합 초기화
		for (int j = 0; j < 3; j++) {                     // 과목 반복
			sum += score[i][j];                           // 점수 합계 계산
		}
		avg[i] = sum / 3.0;                               // 평균 계산 (3.0으로 나눠 double 처리)
	}

	// 최고 평균 점수를 가진 학생 찾기
	for (int i = 1; i < 3; i++) {                         // 두 번째 학생부터 비교
		if (avg[i] > avg[bestIndex]) {                    // 현재 최고 평균보다 크면
			bestIndex = i;                                // 최우수 학생 인덱스 갱신
		}
	}

	// 결과 출력
	cout << "최우수 학생은 " << bestIndex + 1 << "번째 학생이고 평균점수는 " << avg[bestIndex] << " 점이다.";
}